// ============================================================================
// Wizard - SERVICE
//
// This service contains the primary functions that are used across all
// Wizard components.
// ============================================================================

// ----------------------------------------------------------------------------
// Imports
// ----------------------------------------------------------------------------
// Angular
import { Injectable } from "@angular/core";

// ----------------------------------------------------------------------------
// Class
// ----------------------------------------------------------------------------
@Injectable()
export class WizardService {
  // Initialize view
  view: string = "user";

  // ------------------------------------------------------
  // Select view in Wizard
  // ------------------------------------------------------
  SelectView(newView: string) {
    view = newView;
  }
}
