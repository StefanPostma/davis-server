// ----------------------------------------------------------------------------
// Imports
// ----------------------------------------------------------------------------
// Angular
import { Injectable } from "@angular/core";

// ----------------------------------------------------------------------------
// Class
// ----------------------------------------------------------------------------
@Injectable()
export class ConfigService {
  // Initialize view
  view: string = "user";

  // ------------------------------------------------------
  // Select view in Wizard
  // ------------------------------------------------------
  SelectView(newView: string) {
    view = newView;
  }
}
